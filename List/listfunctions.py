a = []
b = [1]
a.append(1)	#Adds an element at the end of the list
a.clear()	    #Removes all the elements from the list
a.copy()	    #Returns a copy of the list
a.count(1)	    #Returns the number of elements with the specified value
a.extend(b)	#Add the elements of a list (or any iterable), to the end of the current list
a.index(1)	#Returns the index of the first element with the specified value
a.insert(1, 1)	#Adds an element at the specified position
a.pop()	        #Removes the element at the specified position
a.remove(1)	#Removes the item with the specified value
a.reverse()	#Reverses the order of the list
a.sort()	        #Sorts the list

